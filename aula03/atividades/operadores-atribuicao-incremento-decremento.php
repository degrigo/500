<?php

echo "<pre>";

$num = 8;

//Soma 
$num += 5;
echo $num;
echo "<hr>";

//Subtração
$num -= 3;
echo $num;
echo "<hr>";

//Multiplicação
$num *= 10;
echo $num;
echo "<hr>";

//Divisão
$num /= 2;
echo $num;
echo "<hr>";

// Incremento
for ($cont = 0; $cont <= 10; $cont++) { 
	echo $cont;
	echo "<br>";
}

echo "<hr>";

// Decremento
for ($cont = 100; $cont >= 89; $cont--) { 
	echo $cont;
	echo "<br>";
}

echo "<hr>";